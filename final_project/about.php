  <h3>About Seafoam Gaming!</h3>
		
		<p style="text-align: center;"><strong>The Origin of Seafoam Gaming</strong></p>
<p>Seafoam Gaming was created in 2014 by me (Connor N), as an experimental attempt to revive a dying forum which I created back in 2010. This forum was meant solely for me and a few friends I made over the internet, but since it was made on a free forum creator, it died off very quickly and barely was kept alive.</p>
		
<p>The experiment began when I reached out to a developer from the Western United States to ask for a review code to their upcoming Wii U eShop game, Chubbins. I started with this game because I was browing my Wii U's store one day and noticed that the game seemed to have came out of nowhere, with nobody else talking about it, and no other reviews out on the internet. Thus, I figured I would offer to give it a look, and the developers agreed, thus starting my career.</p>
		
		<p class="center"><img src="images/dahku-logo3.png" alt="door image" width="82" height="105"  /></p>

		<p>This review, despite being <a href="https://seafoamgaming.com/2014/06/03/chubbins-wii-u-eshop-review/" >pretty terrible</a> by my modern standards, ended up giving my formerly dead site some life. What used to never get views at all, now was getting views! </p>
		
<p style="text-align: center;"><strong>The Evolution</strong></p>
	
		<p>Eventually, I focused more and more on making video game reviews. Honing my writing skills, reaching out to other developers, eventually I got even more review copies and established a lot of contacts in the gaming industry. But there was still a pretty big problem, and that came from how my site was still a forum, a format that isn't really the best for sharing a written, professional review.</p>
		
